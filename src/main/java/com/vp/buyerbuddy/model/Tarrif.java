package com.vp.buyerbuddy.model;

public class Tarrif {

	private String tarrifId;
	private String htsNo;
	private String importCountry;
	private String productName;
	private String productDesc;
	
	public String getTarrifId() {
		return tarrifId;
	}
	public void setTarrifId(String tarrifId) {
		this.tarrifId = tarrifId;
	}
	public String getHtsNo() {
		return htsNo;
	}
	public void setHtsNo(String htsNo) {
		this.htsNo = htsNo;
	}
	public String getImportCountry() {
		return importCountry;
	}
	public void setImportCountry(String importCountry) {
		this.importCountry = importCountry;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductDesc() {
		return productDesc;
	}
	public void setProductDesc(String productDesc) {
		this.productDesc = productDesc;
	}
	
	
}
